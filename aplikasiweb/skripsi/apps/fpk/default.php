<h2 class="content-title">
    <span>Form Pengajuan Kerja</span>
</h2>

<p>
<b>Form Pengajuan Kerja (FPK)</b> adalah form yang berisi pengajuan rencana kerja guna memperpanjang masa berlaku surat kendaraan
yang akan habis masa berlakukanya.Form ini diajukan oleh petugas lapangan atau staf lapangan kepada manager untuk terlebih dahulu mendapat persetujuan rencana kerja yang akan dilakukan.
</p>
<p>
Dari menu ini dapat dilakukan update masa berlaku surat kendaraan, berdasarkan Form Pengajuan Kerja (FPK) yang sudah selesai dikerjakan.
</p>